        string pref = ret.substr(0, idx);
        string suff = ret.substr(idx);
        //cout << "pref = " << pref << "\n";
        //cout << "suff = " << suff << "\n";
        sort(suff.begin(), suff.end());
        ret = pref + suff;
        ret = ret.substr(0, ret.size()-1);
